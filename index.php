<div class="card-deck" STYLE="margin: 10px; font-family: 'Abadi';">
  <div class="card text-white bg-danger mb-3">
    <img class="card-img-top" src="http://marinduquetourism.com/wp-content/uploads/2023/03/mask-making.jpeg" alt="">
    <div class="card-body">
      <h5 class="card-title" style="font-family: 'Verdana'; font-weight: bold;">The art of making a Morion mask</h5>
      <p class="card-text">The wood used for carving masks are taken from Dapdap and Santol Trees which are very common in Marinduque. Carving each mask is undoubtedly the hardest part of the process as it involves a lot of effort, it can take many months to finish. The local craftsmen are very careful with every detail and see to it that the masks bear the caucasian features of European colonizers such as deep eyes, beard, a pointed nose and a villainous scowl. Proper measurements are also taken so that the headgears match the actual head of each person. Painting the masks on the other hand can only take a few days or sometimes, weeks. For the finishing touches, the masks are decorated with ornaments like chicken feathers, horse hair, abaca and straw.</p>
    </div>
  </div>
  <div class="card text-white bg-warning mb-3">
    <img class="card-img-top" src="http://marinduquetourism.com/wp-content/uploads/2023/03/kalutang.jpeg" alt="">
    <div class="card-body">
      <h5 class="card-title" style="font-family: 'Verdana'; font-weight: bold;">Kalutang</h5>
      <p class="card-text">One of Marinduque’s more unique cultural charm is Kalutang-playing. The traditional musical instrument, Kalutang, are identical pairs of wood which when banged together produce notes of different pitches and tones. The Kalutang band which consists of 10-12 musicians is considered as a local attraction of Marinduque playing folk songs.</p>
    </div>
  </div>
  <div class="card text-white bg-secondary mb-3">
    <img class="card-img-top" src="http://marinduquetourism.com/wp-content/uploads/2023/05/tubong3.jpg" alt="">
    <div class="card-body">
      <h5 class="card-title"style="font-family: 'Verdana'; font-weight: bold;">Tubong</h5>
      <p class="card-text">It is a traditional welcome ritual, steeped in color and mirth, performed through song and dance; it is only found in Marinduque! Honorees or guests are figuratively turned into kings and queens (if only for a while). They are seated on chairs, signifying the throne, in the middle of a hall; putting on their heads gaily-decorated crowns, handed a “scepter” made from stems of flowering plants, and finally showered with coins for good luck and fresh flowers for good health amid shouts of “VIVAs” and “MABUHAY” (Longlive!). The coins, having been used in the putong ritual, are believed to be potent tokens; honorees are encouraged to grab a few as keepsakes and as good luck charms.</p>
    </div>
  </div>
<div class="card text-white bg-success mb-3">
    <img class="card-img-top" src="http://marinduquetourism.com/wp-content/uploads/2023/04/salubong.jpg" alt="">
    <div class="card-body">
      <h5 class="card-title"style="font-family: 'Verdana'; font-weight: bold;">Salubong</h5>
      <p class="card-text">Salubong: depicts the dawn meeting between the sorrowful Mother Mary and the Resurrected Christ. <br> Starts as two separate processions; one bearing he icon of black-veiled Mater Dolorosa and joined purely by the women, the other bears the icon of the Resurrected Christ and, yes, participated only by men. The two groups meet at a designated place where a child, dressed as an angel, sings Hosannas and Alleluias while being hoisted down from an elevated platform specially prepared for the occasion. The “angel” then lifts the grieving-veil of Mary to reveal her immaculately white dress signifying happiness amid the applause and praises of the crowd.</p>
    </div>
  </div>
  <div class="card text-white bg-primary mb-3">
    <img class="card-img-top" src="http://marinduquetourism.com/wp-content/uploads/2023/04/prusision1.jpg" alt="">
    <div class="card-body">
      <h5 class="card-title"style="font-family: 'Verdana'; font-weight: bold;">Prusisyon ng Miyerkules-Santo/Prusisyon ng Biyernes-Santo</h5>
      <p class="card-text">(Holy Wednesday Procession): wheeled or shoulder-borne carozas or pass of religious icons oftentimes set in a tableau, some of them depicting the Agony and Passion of Jesus Christ and other santos and santas related to the Passion. <br> Prusisyon ng Biyernes-Santo (Good-Friday Procession): highlighted by the caroza called Sepulkro usually placed near the end of the procession. This icon symbolizes the Holy Sepulchre, the cave where Jesus was buried. After the procession, the faithful visits or stands vigil all night long for the Sepulkro at the local church.</p>
    </div>
  </div>
</div>

