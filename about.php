<p style="margin: 50px; margin-left: 300px; margin-right: 300px; ">The province of Marinduque has a total land area of 95,925 hectares, including four major islets and eight minor ones. Sta. Cruz, a 1st class municipality, has the largest area at 24,665 hectares, followed by Torrijos, a 3rd class municipality, at 21,430 hectares. Boac, a 1st class municipality, is the third largest municipality with 21,270 hectares, followed by Gasan and Mogpog, 3rd classmunicipalities, at 11,930 hectares and 8,770 hectares, respectively. A 4th class municipality Buenavista, at 7,680 hectares, has the smallest land area among the six. <br> <br>
Notable in the land use configuration of the province is the large proportion of land that are grasslands and mangrove forests comprising about 40% of total land area, and the very small built- up area comprising of only 1.80% of total land area. All in all, production areas consist only of 33.7%. This indicates an agriculture based but underproducing economy. At the same time, it indicates tremendous potential for natural resource-based economic development and, considering the province’s topography, nature tourism development. <br> <br>
The province of Marinduque has undergone a series of occupations, and fell under administrations of different states, before finally attaining political status as a province. During the Spanish and initial American occupations, Marinduque was made into a sub-corregimiento in the 16th century under Mindoro’s jurisdiction. Later on, in 1671, Marinduque was made into a province, but later on rebuked and made into a sub-province again. Marinduque completely overthrew the last Spanish casadores (i.e. Tagalog Soldiers) through the local struggle led by Martin Lardizabal, who was appointed as the military governor of Marinduque during the said period. Marinduque was able to pull out from Mindoro and the Spanish rule after the deliberate ratification of the ruling classes from its different towns. <br> <br>
During the American occupation, the notable “Battle of Pulang Lupa”, which took place on September 13, 1900, was the first major victory of the Filipino soldiers led by Lt. Col. Maximo Abad against the American contingent under Capt. Devereaux Shields. The said battle that occurred in Torrijos, became the most unexpected and dramatic overturning of the American forces against the Philippine forces. Subsequently, upon the passing of Act No. 125 by the United States – Philippine Commission, the provincial government of Marinduque was established, with Boac as its capital. A year after, by virtue of different Acts from the US-Philippine commission, Marinduque became part of various provinces including Tayabas (currently, Quezon), sub-province, and, ultimately became a province, through Act No. 2880, brought about by Hon. Ricardo Paras, Jr. Boac, Buenavista, Gasan, Mogpog, Torrijos, and Sta. Cruz became the municipalities under the province.
</p>

