<p style="margin: 50px; margin-left: 300px; margin-right: 300px; "> The island province of Marinduque is located in the southern part of Luzon and belong to Region IV- B or MIMAROPA (Mindoro-Marinduque-Romblon-Palawan). It extends at about 170 kilometers southeast of Metro Manila between the Bondoc Peninsula, at the southeastern portion of Oriental Mindoro and Luzon. It is geographically bounded by Tayabas Bay on the north, Mongpong Pass on the northeast, the Tablas Strait on the west, and by the Sibuyan Sea on the south. It is dubbed as the geographical center of the Philippines. </p>

