<div class="card-deck" STYLE="margin: 10px; margin-left: 250px; margin-right: 250px; font-family: 'Abadi';">
  <div class="card bg-light mb-3">
    <img class="card-img-top" src="http://marinduquetourism.com/wp-content/uploads/2023/04/Arrowroot-cookies-700x525-1-700x500.jpeg" alt="">
    <div class="card-body">
      <h5 class="card-title" style="font-family: 'Verdana'; font-weight: bold;">REJANO’S ARROWROOT COOKIES</h5>
      <p class="card-text">Uraro (arrowroot) which was introduced to Marinduque for cultivation during the Galleon trade along with yam, taro and sweet potato, is now one of Sta. Cruz’s growing industries. Arrowroot cookies are Marinduquenos no. 1 pasalubong’, sold almost everywhere.</p>
    </div>
  </div>
  <div class="card text-white bg-dark mb-3">
    <img class="card-img-top" src="http://marinduquetourism.com/wp-content/uploads/2023/04/Pancit-Miki-photo-by-Romeo-Mataac-Jr-700x700-1-650x650.jpeg" alt="">
    <div class="card-body">
      <h5 class="card-title" style="font-family: 'Verdana'; font-weight: bold;">FRESH MIKI</h5>
      <p class="card-text">Marinduque produces the best miki noodles in the Philippines. It stays firm when cooked and only needs very little seasoning as it has its own unique flavor. It can be cooked with a variety of ingredients like pork, chicken, seafood and vegetables. It can also be enjoyed as a soup or stir-fried.
</p>
    </div>
  </div>
</div>

<div class="card-deck" STYLE="margin: 10px; margin-left: 250px; margin-right: 250px; font-family: 'Abadi';">
  <div class="card text-white bg-dark mb-3">
    <img class="card-img-top" src="http://marinduquetourism.com/wp-content/uploads/2023/04/adobong-manok-sa-gata.jpg" alt="">
    <div class="card-body">
      <h5 class="card-title" style="font-family: 'Verdana'; font-weight: bold;">ADOBONG MANOK SA GATA</h5>
      <p class="card-text">Marinduque is generally known as a coconut-producing province. No wonder Marinduquenos depend on the coconut in preparing their food like ubod for lumpia (fresh rolls), young coconut meat used in local cake-making and of course, coconut milk. Coconut poil is also used in cooking. Moreover, tuba or fermented sap from coconut buds, is a favorite local drink. One of the island’s most desired local cuisines is Adobong manok sa gata.</p>
    </div>
  </div>
  <div class="card bg-light mb-3">
    <img class="card-img-top" src="http://marinduquetourism.com/wp-content/uploads/2023/03/ulang-ulang.jpeg" alt="">
    <div class="card-body">
      <h5 class="card-title" style="font-family: 'Verdana'; font-weight: bold;">ULANG ULANG</h5>
      <p class="card-text">This delectable soup blends the sweetness of ubod (coconut palm heart), the sourness of calamansi, and the distinct flavor of crayfish to create a perfect balance of tastes that will make your taste buds dance.</p>
    </div>
  </div>
</div>

<div class="card-deck" STYLE="margin: 10px; margin-left: 250px; margin-right: 250px; font-family: 'Abadi';">
  <div class="card bg-light mb-3">
    <img class="card-img-top" src="http://marinduquetourism.com/wp-content/uploads/2023/04/kari-kari.jpg" alt="">
    <div class="card-body">
      <h5 class="card-title" style="font-family: 'Verdana'; font-weight: bold;">KARI KARI</h5>
      <p class="card-text">This recipe (not to be confused with “Kare”, a popular meat and vegetable recipe served with bagoong) is a native dish from Marinduque. It is similar to “Dinuguan” but the difference is in the minced meat. Originally, Marinduquenos prepared this as “pulutan” for alcoholic drinks, but now served with other delectable foods on special occasions.</p>
    </div>
  </div>
  <div class="card text-white bg-dark mb-3">
    <img class="card-img-top" src="http://marinduquetourism.com/wp-content/uploads/2023/04/canton-panciteria.jpg" alt="">
    <div class="card-body">
      <h5 class="card-title" style="font-family: 'Verdana'; font-weight: bold;">CANTON PANCITERIA (CHI-WING)
</h5>
      <p class="card-text">Canton Panciteria, popularly known as Chi-Wings (pronounced as chu-wing), has been part and parcel of
the town’s gastronomic culture. It still serves its traditional pancit canton specialty. A vacation on the Heritage district
is never complete without a trip to Chi Wing.</p>
    </div>
  </div>
</div>

<div class="card-deck" STYLE="margin: 10px; margin-left: 250px; margin-right: 250px; font-family: 'Abadi';">
  <div class="card text-white bg-dark mb-3">
    <img class="card-img-top" src="http://marinduquetourism.com/wp-content/uploads/2023/04/bibingkang-lalaki-650x650.jpg" alt="">
    <div class="card-body">
      <h5 class="card-title" style="font-family: 'Verdana'; font-weight: bold;">BIBINGKANG LALAKI (BIBINGKANG BOAC)
</h5>
      <p class="card-text">The staple rice cake dish bibingka, popular across the Philippines, is available in several native varieties throughout the island province, with Bibingkang Lalaki (male bibingka) being just one of them. The name of the province’s capital, Bibingkang Boac, is sometimes used to designate to Bibingkang Lalaki. The amount of bibingka is enormous; it is the size of a pizza. Locals will jokingly expound that this particular variety of bibingka is so named because it includes eggs. Additionally, bibingkang lalaki utilizes coconut wine rather than yeast to keep the interior
wet after cooking.</p>
    </div>
  </div>
  <div class="card bg-light mb-3">
    <img class="card-img-top" src="http://marinduquetourism.com/wp-content/uploads/2023/04/panganan.jpg" alt="">
    <div class="card-body">
      <h5 class="card-title" style="font-family: 'Verdana'; font-weight: bold;">PANGANAN (POWDERED ARROWROOT SNACK)</h5>
      <p class="card-text">With the abundance of arrowroot in Marinduque, it’s only natural that they have a native delicacy like panganan. Panganan is a dry powdery snack made from arrowroot powder and rice flour, similar to minatamis na bao, which is created from sugarcane jaggery that has been melted. Visitors to Marinduque may purchase panganan in travel-size containers for a tasty snack while on the road. It’s widely accessible in the province’s marketplaces.</p>
    </div>
  </div>
</div>

<div class="card-deck" STYLE="margin: 10px; margin-left: 250px; margin-right: 250px; font-family: 'Abadi';">
  <div class="card bg-light mb-3">
    <img class="card-img-top" src="http://marinduquetourism.com/wp-content/uploads/2023/04/sinaludsod.jpg" alt="">
    <div class="card-body">
      <h5 class="card-title" style="font-family: 'Verdana'; font-weight: bold;">SALUDSUD (NATIVE PANCAKE)</h5>
      <p class="card-text">Saludsod are the native pancakes of Marinduque. It’s a popular local dish that tourists to Marinduque must sample because they won’t find it anyplace else in the Philippines. This native pancake is produced with a particular batter made with indigenous components such as arrowroot powder, coconut milk, flour, and sugar.

 

The batter is then baked on a heated pan in the manner of a traditional pancake. Saludsod, on the other hand, has a

sweeter flavor and a creamy/buttery texture.</p>
    </div>
  </div>
  <div class="card text-white bg-dark mb-3">
    <img class="card-img-top" src="http://marinduquetourism.com/wp-content/uploads/2023/04/manakla-650x650.jpg" alt="">
    <div class="card-body">
      <h5 class="card-title" style="font-family: 'Verdana'; font-weight: bold;">MANAKLA (CRAYFISH DISH)</h5>
      <p class="card-text">Marinduque cuisine is a seafood lover’s paradise. Manakla is the indigenous Marinduque term for crayfish, and it’s a type of crayfish that’s very popular in the island. It is frequently utilized as a protein in every cuisine imaginable. Manakla is commonly served in pulutan dishes or as beer/alcohol appetizers at beach taverns and restaurants.</p>
    </div>
  </div>
</div>

